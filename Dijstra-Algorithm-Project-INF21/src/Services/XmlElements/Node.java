package Services.XmlElements;

public class Node {
    private int id;
    private int lat;
    private int lon;

    public void setId(int id) {
        this.id = id;
    }

    public void setLat(int lat) {
        this.lat = lat;
    }

    public void setLon(int lon) {
        this.lon = lon;
    }

    public int getId() {
        return id;
    }

    public int getLat() {
        return lat;
    }

    public int getLon() {
        return lon;
    }
}
